#include "3-calc.h"

/**
 * op_add - Adds two integere
 * @a: First integer
 * @b: Second integer
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integere
 * @a: First integer
 * @b: Second integer
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two integere
 * @a: First integer
 * @b: Second integer
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two integere
 * @a: First integer
 * @b: Second integer
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the remainder of two integere
 * @a: First integer
 * @b: Second integer
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
